package clue;

import java.awt.Color;
import java.awt.Graphics;
import java.lang.reflect.Field;
import java.util.HashSet;

import javax.swing.JPanel;

public abstract class Player extends JPanel {
	private String name;
	private Color color;
	private int index;
	private HashSet<Card> myCards;

	public Player(String name, String color, int index) {
		this.name = name;
		this.index = index;

		try {
			// We can use reflection to convert the string to a color
			Field field = Class.forName("java.awt.Color").getField(color.trim());
			this.color = (Color)field.get(null);
		} catch (Exception e) {
			this.color = null;  // Not defined
		}
		myCards = new HashSet<Card>();
	}

	public String getName() {
		return name;
	}

	public Card disproveSuggestion(HashSet<Card> suggestion) {
		HashSet<Card> returnCards = new HashSet<Card>();
		for (Card card : suggestion) {
			if (myCards.contains(card)) {
				returnCards.add(card);
			}
		}
		return ClueGame.randomCard(returnCards);
	}

	public boolean isComputerPlayer() {
		return false;
	}

	public void setIndex(int index) {
		this.index = index;
	}

	public int getIndex() {
		return index;
	}

	// Automatically generated by Eclipse.
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((color == null) ? 0 : color.hashCode());
		result = prime * result + index;
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj) {
			return true;
		}
		if (obj == null) {
			return false;
		}
		if (getClass() != obj.getClass()) {
			return false;
		}
		Player other = (Player) obj;
		if (color == null) {
			if (other.color != null) {
				return false;
			}
		} else if (!color.equals(other.color)) {
			return false;
		}
		if (index != other.index) {
			return false;
		}
		if (name == null) {
			if (other.name != null) {
				return false;
			}
		} else if (!name.equals(other.name)) {
			return false;
		}
		return true;
	}

	@Override
	public String toString() {
		return "Player [name=" + name + ", color=" + color + ", index=" + index
				+ "]";
	}

	public void addCard(Card card) {
		myCards.add(card);
	}

	public void paintComponent(Graphics g, int numRows, int numColumns) {
		super.paintComponent(g);
		g.setColor(color);
		int row = index / numRows;
		int col = index % numColumns;
		g.fillOval(col*20+21, row*20+21, 16, 16);
	}

	// For use by unit tests only.
	public HashSet<Card> getCards() {
		return myCards;
	}
}
